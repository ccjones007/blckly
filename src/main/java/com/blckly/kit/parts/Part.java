package com.blckly.kit.parts;

public class Part {
  private String name;
  private double surcharge;

  public Part() {}

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public double getSurcharge() {
    return surcharge;
  }

  public void setSurcharge(double surcharge) {
    this.surcharge = surcharge;
  }
}
